void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	a;

	i = 0;
	while (i < size - 1)
	{
		if (*(tab + i) > *(tab + i + 1))
		{
			i++;
			while ((i > 0) && (*(tab + i) < *(tab + i - 1)))
			{
				a = *(tab + i - 1);
				*(tab + i - 1) = *(tab + i);
				*(tab + i) = a;
				i--;
			}
		}
		i++;
	}
}
