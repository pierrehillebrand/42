int	ft_iterative_factorial(int nb)
{
	int	i;
	int	x;

	i = 0;
	if (nb < 0)
		return (0);
	x = 1;
	while (i < nb - 1)
	{
		x *= nb - i;
		i++;
	}
	return (x);
}
