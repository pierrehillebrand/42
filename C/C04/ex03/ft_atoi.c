int	ft_atoi(char *str)
{
	int			i;
	int			test;
	long int	y;

	i = 0;
	while ((str[i] <= 13 && str[i] >= 9) || str[i] == 32)
		i++;
	test = 1;
	y = 0;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			test = -test;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9' && str[i] != '\0')
	{
		y = y * 10 + str[i] - 48;
		i++;
	}
	return (test * y);
}
